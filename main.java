package ngan;

public class main {
	String maSV;
	String ten;
	String nam;
	public main(String maSV, String ten, String nam) {
		super();
		this.maSV = maSV;
		this.ten = ten;
		this.nam = nam;
	}
	public String getMaSV() {
		return maSV;
	}
	public void setMaSV(String maSV) {
		this.maSV = maSV;
	}
	public String getTen() {
		return ten;
	}
	public void setTen(String ten) {
		this.ten = ten;
	}
	public String getNam() {
		return nam;
	}
	public void setNam(String nam) {
		this.nam = nam;
	}
}
